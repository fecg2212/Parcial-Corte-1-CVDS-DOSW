package edu.dosw.lab.Parcial01;

public class Calculadora {

    // Suma dos números
    public int sumar(int a, int b) {
        return a + b;
    }

    // Resta dos números
    public int restar(int a, int b) {
        return a - b;
    }

    // Devuelve si un número es par
    public boolean esPar(int numero) {
        return numero % 2 == 0;
    }
}

